class FoodItem {
  const FoodItem({
    required this.name,
    required this.brand,
    required this.kcal,
    required this.protein,
    required this.carbs,
    required this.fat,
  });

  final String name;
  final String brand;
  final int kcal;
  final int protein;
  final int carbs;
  final int fat;
}
